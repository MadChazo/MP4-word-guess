var timeLeft = 30;
var currentWord =
  wordList[Math.floor(Math.random() * wordList.length)].split("");
var currentBlank = ["_", "_", "_", "_", "_"];
var wins = localStorage.getItem("wins") || 0;
var losses = localStorage.getItem("losses") || 0;

function writeTime() {
  timeSpan.textContent = timeLeft;
}

function end(outcome) {
  if (outcome == "win") {
    wins++;
    alert("You won!");
  } else {
    losses++;
    alert("You lost!");
  }
  localStorage.setItem("wins", wins);
  localStorage.setItem("losses", losses);
  location.reload();
}

function timer() {
  var timer = setInterval(function () {
    timeLeft--;
    writeTime();
    if (timeLeft == 0) {
      clearInterval(timer);
      end("loss");
    }
  }, 1000);
}

function wordGuess(event) {
  var keyPressed = event.key.toLowerCase();
  if (currentWord.includes(keyPressed)) {
    console.log("correct");
    for (let i = 0; i < currentWord.length; i++) {
      if (currentWord[i] == keyPressed) {
        letterList[i].textContent = keyPressed;
        currentBlank[i] = keyPressed;
      }
    }
  } else {
    timeLeft -= 3;
  }
  if (!currentBlank.includes("_")) {
    setTimeout(function () {
      end("win");
    }, 1000);
  }
}

function start() {
  startBtn.removeEventListener("click", start);
  document.addEventListener("keydown", wordGuess);
  timer();
}

function clearScore() {
  localStorage.clear();
  winSpan.textContent = 0;
  lossSpan.textContent = 0;
}

writeTime();
winSpan.textContent = wins;
lossSpan.textContent = losses;

startBtn.addEventListener("click", start);
clearBtn.addEventListener("click", clearScore);
